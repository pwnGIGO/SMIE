                    <h2 align="center">DIRIGIDO A LA COMUNIDAD ESTUDIANTIL</h2>
                    <p>
                        El día jueves 29 de noviembre del 2018, siendo las 10:45 am se tomó protesta de esta sociedad ante las respectivas autoridades contando con la presencia de alumnos así como de la Sociedad Mexicana de Ingeniería Estructural.<br>

                        El capítulo estudiantil de la <strong>Sociedad Mexicana de Ingeniería Estructural (SMIE) de la UNIVERSIDAD AUTÓNOMA METROPOLITANA</strong>, extiende la invitación a alumnos tanto de Ingeniería Civil como de Arquitectura del país y del extranjero para formar parte de este capítulo.<br>

                        Este proyecto tiene como fin apoyar y fortalecer a sus miembros mediante conferencias, visitas guiadas, obras o trabajos que tengan relación con la ingeniería estructural.<br>

                        Para dar reconocimiento a un capitulo estudiantil, se requiere formar un grupo de al menos 10 estudiantes de la carrera, nombrar a un profesor quien fungirá como tutor mismo quien se encargara de obtener las autorizaciones necesarias, tanto de la institución como de la SMIE para hacer una ceremonia de toma de protesta y de esta manera hacerse oficial.<br>

                        Una de las funciones principales de este capítulo estudiantil es organizar dichos eventos relacionados con la ingeniería estructural, en la <strong>UNIVERSIDAD AUTÓNOMA METROPOLITANA UNIDAD AZCAPOTZALCO</strong>, esto con el apoyo de la SMIE.<br>

                        Difundir en las páginas oficiales como redes sociales: noticias, artículos, avisos y comentarios relacionados con la ingeniería estructural. El capítulo está interesado en la participación de alumnos que tengan interés en formar parte de este, ya sea como miembro o como visitante.<br>

